/* Type definitions for better TypeScript support */
export type ColorScale = typeof designTokens.colors.primary;
export type SpacingValue = keyof typeof designTokens.spacing;
export type FontSize = keyof typeof designTokens.typography.fontSize;
export type FontWeight = keyof typeof designTokens.typography.fontWeight;
export type BorderRadius = keyof typeof designTokens.borderRadius;
export type BoxShadow = keyof typeof designTokens.boxShadow;
export type Breakpoint = keyof typeof designTokens.breakpoints;
export type ZIndex = keyof typeof designTokens.zIndex;

/* Utility functions for accessing design tokens */
export const getColor = (color: keyof typeof designTokens.colors, shade: keyof ColorScale = 500) => {
  return designTokens.colors[color][shade];
};

export const getSpacing = (value: SpacingValue) => {
  return designTokens.spacing[value];
};

export const getFontSize = (size: FontSize) => {
  return designTokens.typography.fontSize[size];
};

export const getBorderRadius = (radius: BorderRadius) => {
  return designTokens.borderRadius[radius];
};

export const getBoxShadow = (shadow: BoxShadow) => {
  return designTokens.boxShadow[shadow];
};

export const getBreakpoint = (breakpoint: Breakpoint) => {
  return designTokens.breakpoints[breakpoint];
};

export const getZIndex = (zIndex: ZIndex) => {
  return designTokens.zIndex[zIndex];
};